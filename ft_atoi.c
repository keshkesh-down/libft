int	ft_atoi(const char	*str)
{
	int				i;
	unsigned int	res;

	i = 1;
	while (((*str > 8) && (*str < 14)) | (*str == 32))
		str++;
	if ((*str == '+') || (*str == '-'))
	{
		if (*str == '-')
			i = -1;
		str++;
	}
	res = 0;
	while (*str >= '0' && *str <= '9')
	{
		res = (res * 10) + (*str - '0');
		str++;
	}
	if ((res > 2147483648) && (i == 1))
		return (-1);
	if ((res > 2147483648) && (i == -1))
		return (0);
	return (res * i);
}
